package prov.chartLite;

public class DataContainer {
	int x; 
	int y; 
	int c;
	
	public DataContainer(int x , int y , int z){
		this.x = x;
		this.y = y; 
		this.c = z;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getZ() {
		return c;
	}
	public void setZ(int z) {
		this.c = z;
	}

	
	
}
